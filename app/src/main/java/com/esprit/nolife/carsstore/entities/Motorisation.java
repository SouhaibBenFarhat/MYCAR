public class Motorisation {
    private String motorisationId;
    private String nombreCylindre;
    private String energie;
    private String puissanceFiscal;
    private String puissanceChdin;
    private String couple;
    private String cylindree;
    private String consommationUrbaine;
    private String consommationMixte;
    private String zeroToCent;
    private String vitesseMax;


    public String getMotorisationId() {
        return motorisationId;
    }

    public void setMotorisationId(String motorisationId) {
        this.motorisationId = motorisationId;
    }

    public String getNombreCylindre() {
        return nombreCylindre;
    }

    public void setNombreCylindre(String nombreCylindre) {
        this.nombreCylindre = nombreCylindre;
    }

    public String getEnergie() {
        return energie;
    }

    public void setEnergie(String energie) {
        this.energie = energie;
    }

    public String getPuissanceFiscal() {
        return puissanceFiscal;
    }

    public void setPuissanceFiscal(String puissanceFiscal) {
        this.puissanceFiscal = puissanceFiscal;
    }

    public String getPuissanceChdin() {
        return puissanceChdin;
    }

    public void setPuissanceChdin(String puissanceChdin) {
        this.puissanceChdin = puissanceChdin;
    }

    public String getCouple() {
        return couple;
    }

    public void setCouple(String couple) {
        this.couple = couple;
    }

    public String getCylindree() {
        return cylindree;
    }

    public void setCylindree(String cylindree) {
        this.cylindree = cylindree;
    }

    public String getConsommationUrbaine() {
        return consommationUrbaine;
    }

    public void setConsommationUrbaine(String consommationUrbaine) {
        this.consommationUrbaine = consommationUrbaine;
    }

    public String getConsommationMixte() {
        return consommationMixte;
    }

    public void setConsommationMixte(String consommationMixte) {
        this.consommationMixte = consommationMixte;
    }

    public String getZeroToCent() {
        return zeroToCent;
    }

    public void setZeroToCent(String zeroToCent) {
        this.zeroToCent = zeroToCent;
    }

    public String getVitesseMax() {
        return vitesseMax;
    }

    public void setVitesseMax(String vitesseMax) {
        this.vitesseMax = vitesseMax;
    }
}
